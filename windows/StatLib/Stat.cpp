#include "Stat.h"
#include <stdexcept>

using namespace std;

namespace classStat
{
	double Stat::Add(double a, double b)
	{
		return a + b;
	}

	double Stat::Subtract(double a, double b)
	{
		return a - b;
	}

	double Stat::Multiply(double a, double b)
	{
		return a * b;
	}

	double Stat::Divide(double a, double b)
	{
		return a / b;
	}
}



